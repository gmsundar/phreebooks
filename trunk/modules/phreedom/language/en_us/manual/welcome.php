<h1 align="center">Welcome to PhreeBooks</h1>
<p>PhreeBooks is an open source accounting application designed for ease of use and robust functionality. Written in freely available PHP and MySQL, PhreeBooks has been tested on multiple platforms and browsers to ensure the broadest compatibility possible. </p>
<p>Some of the feature highlights are:</p>
<ul>
  <li> Web based interface</li>
  <li>Full featured accounting engine</li>
  <li>Complete inventory control including assemblies</li>
  <li>Multiple company capablility</li>
  <li> Multiple language capablility</li>
  <li> Robust reporting capability </li>
  <li>Full featured import/export capability</li>
  <li>User friendly installation and setup script</li>
  <li>Extensive user level security </li>
</ul>
